<b>Profil Kelurahan Leuwigajah</b>

Kelurahan Leuwigajah adalah salah satu wilayah kerja Lurah Leuwigajah di Kecamatan Cimahi Selatan Kota Cimahi dan secara geografis memiliki luas wilayah sebesar 393,473 Km<sup>2</sup>. Secara administratif mempunyai 20 RW dan 148 RT dengan jumlah penduduk sebesar 42.790 jiwa (<i>data bulan Mei 2014</i>). Wilayah Kelurahan Leuwigajah merupakan wilayah perdagangan dan jasa, permukiman, pusat pendidikan serta sebagian kecil merupakan wilayah perindustrian.

