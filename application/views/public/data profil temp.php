<b>Visi Dan Misi </b>

Dalam rangka pelaksanaan tugas pemerintahan dan pembangunan serta pelayanan masyarakat, Kelurahan Leuwigajah menetapkan visi yang mendukung terhadap Visi Kota Cimahi <i>“ Menuju Cimahi CERDAS ”</i> yaitu “<i>Menjaga dan mewujudkan masyarakat Leuwigajah yang taat </i><i>IBADAH</i><i>”</i> (Indah, Bersih, Agamis, Demokratis, Aman dan Harmonis) yang dijabarkan sebagai berikut :

-      Indah               : yang berarti mewujudkan keindahan baik akhlak, mental maupun lingkungannya

-      Bersih             : yang berarti budaya hidup bersih baik lingkungan maupun mental yang bersih jauh dari perbuatan yang tercela.

-      Agamis           : yang berarti mewujudkan nilai-nilai keagamanan dalan kehidupan dimasyarakat

-      Demokratis     : yang berarti mengandung pengertian bahwa dalam menyelesaikan permasalahan yang timbul di lingkungan masyarakat harus menjungjung tinggi nilai-nilai musyawarah untuk mufakat yang dilaksanakan secara demokratis

-      Aman              : yang berarti menjaga lingkungan yang aman dan nyaman

-      Harmonis        : yang berarti mewujudkan masyarakat yang dapat hidup berdampingan atau harmonis.

Dalam upaya perwujudan Visi Kelurahan Leuwigajah Tahun 2012 – 2017 tersebut akan dicapai melalui 5 (lima) misi sebagai berikut :
<ol>
	<li>Meningkatkan kualitas pelayanan kepada masyarakat</li>
</ol>
Yaitu peningkatan pelayanan yang lebih efektif dan efisien guna mempercepat pelayanan pemerintahan kepada masyarakat.
<ol>
	<li>Meningkatkan pemberdayaan masyarakat dalam pembangunan</li>
</ol>
Yaitu mendorong terciptanya kemandirian masyarakat dalam ikut serta berpartisipasi dalam pembangunan diwilayahnya supaya lebih mandiri dan berkesinambungan.
<ol>
	<li>Meningkatkan peran sumber daya aparatur dan masyarakat dalam  pembangunan</li>
</ol>
Yaitu meningkatkan kapasitas aparatur dan peran serta masyarakat dalam menumbuh kembangkan inovasi kreatif dalam pembangunan diwilayahnya.
<ol>
	<li>Meningkatkan situasi dan kondisi masyarakat yang kondusif.</li>
</ol>
Yaitu mengoptimalkan pengamanan keamanan di lingkungannya secara bersama - sama sehingga mendukung lancarnya pembangunan.
<ol>
	<li>Meningkatkan kualitas sumber daya aparatur</li>
</ol>
Yaitu dengan meningkatkan kualitas aparatur yang profesional sehingga dapat mewujudkan kualitas pelayanan yang semakin baik.

